import { Target, Plus, Pencil, Trash2, Loader2, BarChart3, Rocket, Settings2 } from 'lucide-react';
import { Dialog, DialogContent, DialogHeader, DialogTitle, DialogTrigger } from '@/components/ui/dialog';
import { useQuery, useMutation, useQueryClient } from '@tanstack/react-query';
import { api } from '@/lib/api-client';
import { toast } from 'sonner';
import { QueryError } from '@/components/ui/query-error';
import { useT } from '@/contexts/LanguageContext';

interface MissionTemplate {
  id: string;
  title: string;
  description: string;
  type: string;
  difficulty: string;
  reward_coins: number;
  reward_points: number;
  target_amount: number | null;
  age_group: string;
  is_active: boolean;
  conditions: Record<string, unknown>;
  created_at: string;
}

function useTemplates() {
  return useQuery({
    queryKey: ['mission-templates'],
    queryFn: async () => {
      const res = await api.get<any>('/mission-templates');
      const data = Array.isArray(res) ? res : (res?.data ?? []);
      return data as MissionTemplate[];
    },
  });
}

function useMissionAnalytics() {
  return useQuery({
    queryKey: ['mission-analytics'],
    queryFn: async () => {
      const res = await api.get<any>('/missions?fields=status,type,difficulty,is_auto_generated,source');
      const missions = Array.isArray(res) ? res : (res?.data ?? []);
      const total = missions.length;
      const completed = missions.filter((m: any) => m.status === 'completed').length;
      const autoGenerated = missions.filter((m: any) => m.is_auto_generated).length;
      const byType: Record<string, number> = {};
      const byDifficulty: Record<string, number> = {};
      missions.forEach((m: any) => {
        byType[m.type] = (byType[m.type] ?? 0) + 1;
        byDifficulty[m.difficulty ?? 'beginner'] = (byDifficulty[m.difficulty ?? 'beginner'] ?? 0) + 1;
      });
      return { total, completed, completionRate: total ? Math.round((completed / total) * 100) : 0, autoGenerated, byType, byDifficulty };
    },
  });
}

const TYPES = ['saving', 'budgeting', 'planning', 'learning', 'social', 'goal', 'daily', 'weekly'];
const DIFFICULTIES = ['beginner', 'explorer', 'saver', 'strategist', 'master'];
const AGE_GROUPS = ['all', '5-7', '8-12', '13-17'];

const difficultyColor: Record<string, string> = {
  beginner: 'bg-secondary/20 text-secondary',
  explorer: 'bg-primary/20 text-primary',
  saver: 'bg-accent/20 text-accent-foreground',
  strategist: 'bg-[hsl(var(--kivara-light-gold))] text-accent-foreground',
  master: 'bg-destructive/20 text-destructive',
};

